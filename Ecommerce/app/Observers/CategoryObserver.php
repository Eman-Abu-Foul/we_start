    /**
     * Handle the Category "created" event.
     *
     * @param  \App\Models\Category  $category
     * @return void
     */
    function creating($value) {

        $slug = Str::slug(request()->en_name);

        // $count = Category::whereSlug($slug)->count();
        $count = Category::where('slug', 'like', $slug.'%')->count();
        if($count) {
            $slug = $slug.'-'.$count;
        }

        $value->slug = $slug;
    }

