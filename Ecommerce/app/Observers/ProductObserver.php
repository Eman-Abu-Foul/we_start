    /**
     * Handle the Product "created" event.
     *
     * @param  \App\Models\Product  $product
     * @return void
     */
    function creating($value) {

        $slug = Str::slug(request()->en_name);

        // $count = Category::whereSlug($slug)->count();
        $count = Product::where('slug', 'like', $slug.'%')->count();
        if($count) {
            $slug = $slug.'-'.$count;
        }

        $value->slug = $slug;
    }

